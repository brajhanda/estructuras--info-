
package Modelo;

public class Capital {
     
    String Nombre;
    String Cedula;
    int CapInicial;
    
   

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getCedula() {
        return Cedula;
    }

   
    public void setCedula(String Cedula) {
        this.Cedula = Cedula;
    }

    public int getCapInicial() {
        return CapInicial;
    }

    public void setCapInicial(int CapInicial) {
        this.CapInicial = CapInicial;
    }

    
  
    
}
